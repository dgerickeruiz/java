public class Variable {
    public static void main (String[] args) {
        int numero = 1;
        String nombre = "Ibra";
        char letra = 'l';
        double temperatura = 38.5;
        boolean sioNo = false;
        long largo = 123456789;

        System.out.println("Entero es: " + numero);
        System.out.println("String es: " + nombre);
        System.out.println("Caracter es: " + letra);
        System.out.println("Double es: " + temperatura);
        System.out.println("Booleano es: " + sioNo);
        System.out.println("Long es: " + largo);
      }
}
