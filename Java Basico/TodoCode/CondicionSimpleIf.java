public class CondicionSimpleIf {

    public static void main(String[] args) {
        int numl = 5;
        int num2 = 10;

        System.out.println();
        if (num2 > numl) {
            System. out.println("El num2 es mayor al numl");
        }

        numl = 4 ; num2 = 2;
        System.out.println();
        if (num2 < numl) {
            System. out.println("El num2 es menor al numl");
        }
        System.out.println();
    }
}
